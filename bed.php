                                            <h2 style="margin-top: 30px; margin-bottom: 20px;">About B.Ed Course</h2>
                                            <p>B.Ed. course is the perfect course which you can opt to make your career.
                                                Bachelor of Education popularly referred as B.Ed., is a two year
                                                full-time degree course. This course has been designed for those
                                                aspirants who are interested to pursue a career in teaching and other
                                                related fields. For teaching in high schools and upper primary schools,
                                                B.Ed. a degree is a must require in India. At present, the need for
                                                trained teachers is very high as a lot of schools are building day by
                                                day. To become a successful professional in the teaching field, one
                                                should have a minimum qualification in Bachelor of Education (B.Ed.).
                                                After completing the B.Ed. degree you will become a “Trained Teacher”
                                            </p>

                                            <h4>Eligibility Criteria</h4>
                                            <p>Candidates with at least 50% marks (47.5 % in case of
                                                SC/ST/Blind/Visually and Differently Abled Candidates of Haryana Only)
                                                either in the Bachelor Degree and/or in the Master’s Degree in
                                                Sciences/Social Sciences/ Humanity, Bachelor’s in Engineering or
                                                Technology with specialization in Science and Mathematics with 55% marks
                                                (52.25% in case of SC/ST/Blind/Visually and Differently Abled Candidates
                                                of Haryana Only) or any other qualification equivalent thereto*, are
                                                eligible for admission to the program. *BBA/MBA with 50% marks (47.5% in
                                                case of SC/ST/Blind/Visually and Differently Abled Candidates of Haryana
                                                Only) and BCA/MCA with 55% marks 52.25% in case of SC/ST/Blind/Visually
                                                and Differently Abled Candidates of Haryana Only ”.</p>

